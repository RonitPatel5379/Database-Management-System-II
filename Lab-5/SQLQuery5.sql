--5. Create a cursor Course_CursorUpdate that retrieves all courses and increases Credits by 1 for courses with Credits less than 4. 
GO
DECLARE COURSE_CURSORUPDATE CURSOR
FOR
SELECT COURSEID, COURSECREDITS FROM COURSE;

DECLARE @CID VARCHAR(10), @CREDIT INT;

OPEN COURSE_CURSORUPDATE;
FETCH NEXT FROM COURSE_CURSORUPDATE INTO @CID, @CREDIT;

WHILE @@FETCH_STATUS = 0
BEGIN
    IF @CREDIT < 4
        UPDATE COURSE
        SET COURSECREDITS = COURSECREDITS + 1
        WHERE COURSEID = @CID;

    FETCH NEXT FROM COURSE_CURSORUPDATE INTO @CID, @CREDIT;
END

CLOSE COURSE_CURSORUPDATE;
DEALLOCATE COURSE_CURSORUPDATE;


--6. Create a Cursor to fetch Student Name with Course Name (Example: Raj Patel is enrolled in Database Management System) 
DECLARE STUDENT_COURSE_CURSOR CURSOR
FOR
SELECT S.STUNAME, C.COURSENAME
FROM STUDENT S
JOIN ENROLLMENT E ON S.STUDENTID = E.STUDENTID
JOIN COURSE C ON E.COURSEID = C.COURSEID;

DECLARE @SNAME VARCHAR(100), @CNAME VARCHAR(100);

OPEN STUDENT_COURSE_CURSOR;
FETCH NEXT FROM STUDENT_COURSE_CURSOR INTO @SNAME, @CNAME;

WHILE @@FETCH_STATUS = 0
BEGIN
    PRINT @SNAME + ' IS ENROLLED IN ' + @CNAME;
    FETCH NEXT FROM STUDENT_COURSE_CURSOR INTO @SNAME, @CNAME;
END

CLOSE STUDENT_COURSE_CURSOR;
DEALLOCATE STUDENT_COURSE_CURSOR;

