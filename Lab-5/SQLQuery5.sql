--1. Create a cursor Course_Cursor to fetch all rows from COURSE table and display them. 
DECLARE COURSE_CURSOR CURSOR
FOR
SELECT COURSEID, COURSENAME, COURSECREDITS
FROM COURSE;

DECLARE @CID VARCHAR(10), @CNAME VARCHAR(100), @CREDITS INT;

OPEN COURSE_CURSOR;
FETCH NEXT FROM COURSE_CURSOR INTO @CID, @CNAME, @CREDITS;

WHILE @@FETCH_STATUS = 0
BEGIN
    PRINT @CID + ' - ' + @CNAME + ' - ' + CAST(@CREDITS AS VARCHAR);
    FETCH NEXT FROM COURSE_CURSOR INTO @CID, @CNAME, @CREDITS;
END
 
CLOSE COURSE_CURSOR;
DEALLOCATE COURSE_CURSOR;


