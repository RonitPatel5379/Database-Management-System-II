--8. Create a cursor to update all NULL grades to 'F' for enrollments with Status 'Completed' 
DECLARE GRADE_CURSOR CURSOR
FOR
SELECT ENROLLMENTID
FROM ENROLLMENT
WHERE GRADE IS NULL
AND ENROLLMENTSTATUS = 'COMPLETED';

DECLARE @EID INT;

OPEN GRADE_CURSOR;
FETCH NEXT FROM GRADE_CURSOR INTO @EID;

WHILE @@FETCH_STATUS = 0
BEGIN
    UPDATE ENROLLMENT
    SET GRADE = 'F'
    WHERE ENROLLMENTID = @EID;

    FETCH NEXT FROM GRADE_CURSOR INTO @EID;
END

CLOSE GRADE_CURSOR;
DEALLOCATE GRADE_CURSOR;

