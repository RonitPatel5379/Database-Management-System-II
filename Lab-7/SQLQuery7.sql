--9.Create trigger to Allow enrolment in month from Jan to August, otherwise print message enrolment closed.
	GO
    CREATE OR ALTER TRIGGER TRIG_ENROLLMENT_CHECK
	ON ENROLLMENT
	INSTEAD OF INSERT
	AS
	BEGIN
		DECLARE @MONTH INT = MONTH(GETDATE())
		IF @MONTH NOT BETWEEN 1 AND 8
		BEGIN
			PRINT 'ENROOLMENT CLOSED'
			RETURN
		END
		INSERT INTO ENROLLMENT
		SELECT * FROM ENROLLMENT
	END
--10.Create trigger to Allow only grade change in enrollment (block other updates)
	GO
	CREATE OR ALTER TRIGGER TRIG_ENROLLMENT_CHANGE
	ON ENROLLMENT
	INSTEAD OF UPDATE
	AS
	BEGIN
		IF UPDATE(StudentID) OR UPDATE(CourseID) OR UPDATE(EnrollmentDate)
		BEGIN
			PRINT 'ONLY GRADE CHANGE IS ALLOWED.'
			RETURN
		END
		UPDATE ENROLLMENT
		SET Grade = inserted.Grade
		FROM ENROLLMENT
		INNER JOIN inserted ON ENROLLMENT.EnrollmentID = inserted.EnrollmentID
	END