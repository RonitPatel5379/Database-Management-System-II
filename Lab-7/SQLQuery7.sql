--7.Create trigger for unique facultyís email check.
	GO
	CREATE OR ALTER TRIGGER TRIG_FACULTY_EMAIL
	ON FACULTY
	INSTEAD OF INSERT
	AS
	BEGIN
		IF EXISTS (
			SELECT 1 FROM FACULTY JOIN inserted ON FACULTY.FacultyEmail = inserted.FacultyEmail
		)
		BEGIN
			PRINT 'FACULTY EMAIL MUST BE UNIQUE.'
			RETURN
		END
		INSERT INTO FACULTY SELECT * FROM inserted
	END
--8.Create trigger for preventing duplicate enrollment.
	GO
	CREATE OR ALTER TRIGGER TRIG_DUPLICATE_ENROLLMENT
	ON ENROLLMENT
	INSTEAD OF INSERT
	AS
	BEGIN
		IF EXISTS (
			SELECT 1 FROM ENROLLMENT JOIN inserted ON ENROLLMENT.StudentID = inserted.StudentID
			AND ENROLLMENT.CourseID = inserted.CourseID
		)
		BEGIN
			PRINT 'DUPLICATE ENROLLMENT IS NOT ALLOWED'
			RETURN
		END
		INSERT INTO ENROLLMENT SELECT * FROM inserted
	END
